def masked_loss(

    logits,

    target,

    puzzle

):

    B = logits.shape[0]

    logits = logits.reshape(

        B*81,

        9

    )

    target = target.reshape(

        B*81

    )

    puzzle = puzzle.reshape(

        B*81

    )

    losses = criterion(

        logits,

        target

    )

    mask = (

        puzzle == 0

    ).float()

    loss = (

        losses * mask

    ).sum() / (

        mask.sum() + 1e-8

    )

    return loss

