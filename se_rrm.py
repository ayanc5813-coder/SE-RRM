def count_solutions(board, limit=2):

    empty = find_empty(board)

    if empty is None:
        return 1

    row,col = empty

    total = 0

    for num in range(1,10):

        if valid(board,row,col,num):

            board[row,col]=num

            total += count_solutions(board,limit)

            board[row,col]=0

            if total >= limit:
                break

    return total

