def fill_board(board):

    empty = find_empty(board)

    if empty is None:
        return True

    row, col = empty

    numbers = list(range(1, 10))
    random.shuffle(numbers)

    for num in numbers:

        if valid(board, row, col, num):

            board[row][col] = num

            if fill_board(board):
                return True

            board[row][col] = 0

    return False

