class SudokuEmbedding(nn.Module):

    def __init__(self):

        super().__init__()

        self.token_embedding = nn.Embedding(
            VOCAB_SIZE,
            EMBED_DIM
        )

        self.position_embedding = nn.Parameter(
            torch.randn(
                1,
                SEQ_LEN,
                EMBED_DIM
            )
        )

    def forward(self,x):

        token = self.token_embedding(x)

        return token + self.position_embedding

class FeedForward(nn.Module):

    def __init__(self):

        super().__init__()

        self.net = nn.Sequential(

            nn.Linear(
                EMBED_DIM,
                HIDDEN_DIM
            ),

            nn.GELU(),

            nn.Dropout(DROPOUT),

            nn.Linear(
                HIDDEN_DIM,
                EMBED_DIM
            )

        )

    def forward(self,x):

        return self.net(x)

class AttentionBlock(nn.Module):

    def __init__(self):

        super().__init__()

        self.norm = nn.LayerNorm(
            EMBED_DIM
        )

        self.attn = nn.MultiheadAttention(

            embed_dim=EMBED_DIM,

            num_heads=NUM_HEADS,

            dropout=DROPOUT,

            batch_first=True

        )

    def forward(self,x):

        y = self.norm(x)

        out,_ = self.attn(

            y,

            y,

            y,

            need_weights=False

        )

        return x + out

class FFNBlock(nn.Module):

    def __init__(self):

        super().__init__()

        self.norm = nn.LayerNorm(

            EMBED_DIM

        )

        self.ff = FeedForward()

    def forward(self,x):

        return x + self.ff(

            self.norm(x)

        )

class ReasoningBlock(nn.Module):

    def __init__(self):

        super().__init__()

        self.attn = AttentionBlock()

        self.ffn = FFNBlock()

    def forward(self,x):

        x = self.attn(x)

        x = self.ffn(x)

        return x

class RecurrentReasoner(nn.Module):

    def __init__(self):

        super().__init__()

        self.block = ReasoningBlock()

    def forward(

        self,

        x,

        steps=REASONING_STEPS

    ):

        for _ in range(steps):

            x = self.block(x)

        return x

class PredictionHead(nn.Module):

    def __init__(self):

        super().__init__()

        self.norm = nn.LayerNorm(

            EMBED_DIM

        )

        self.linear = nn.Linear(

            EMBED_DIM,

            NUM_CLASSES

        )

    def forward(self,x):

        x = self.norm(x)

        return self.linear(x)

