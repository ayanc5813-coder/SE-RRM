def masked_accuracy(

    logits,

    target,

    puzzle

):

    pred = logits.argmax(-1)

    mask = puzzle == 0

    correct = (

        pred == target

    ) & mask

    acc = (

        correct.sum().float()

        /

        (mask.sum()+1e-8)

    )

    return acc.item()

