def multi_step_loss(outputs, target, puzzle, weights=None):

    if weights is None:

        weights = torch.linspace(0.3, 1.0, len(outputs)).to(DEVICE)

    total_loss = 0

    for i, logits in enumerate(outputs):

        loss = masked_loss(logits, target, puzzle)

        total_loss += weights[i] * loss

    return total_loss / weights.sum()

