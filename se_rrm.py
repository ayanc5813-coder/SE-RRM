import ast

def parse_llm_output(text):

    try:

        return ast.literal_eval(text)

    except:

        # fallback safe parse

        nums = [int(x) for x in text if x.isdigit()]

        return nums[:9]

def get_llm_ranking(board, row, col):

    prompt = build_prompt(board, row, col)

    response = call_openrouter(prompt)

    ranking = parse_llm_output(response)

    return ranking

def is_uncertain(probs, threshold=0.4):

    top2 = np.sort(probs)[-2:]

    gap = top2[1] - top2[0]

    return gap < threshold

def hybrid_rank(board, row, col):

    probs = get_cell_probs(board)

    idx = cell_index(row, col)

    cell_probs = probs[idx]

    # neural ranking
    neural_rank = get_ranked_digits(cell_probs)

    # uncertainty check
    if is_uncertain(cell_probs):

        try:

            llm_rank = get_llm_ranking(board, row, col)

            return llm_rank

        except:

            return neural_rank

    return neural_rank

def solve_backtracking_hybrid(board, stats):

    stats.nodes += 1

